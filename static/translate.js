// Function to apply translations to both text content and placeholders
function applyTranslations() {
    let selectedLang = localStorage.getItem("chatbotLanguage") || "english";

    document.querySelectorAll("[data-translate]").forEach(element => {
        let key = element.getAttribute("data-translate");

        if (translations[selectedLang] && translations[selectedLang][key]) {
            let translatedText = translations[selectedLang][key];

            // Handle input fields separately (change placeholder instead of text)
            if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
                element.placeholder = translatedText;
            } else {
                element.textContent = translatedText;
            }

            // Special case: Translating the dynamic welcome message
            if (key === "welcome_message") {
                let userName = element.getAttribute("data-name") || "User";
                let userAge = element.getAttribute("data-age") || "Unknown";
                let userGender = element.getAttribute("data-gender") || "Not specified";

                // Translate gender if available
                if (translations[selectedLang][userGender.toLowerCase()]) {
                    userGender = translations[selectedLang][userGender.toLowerCase()];
                }

                // Replace placeholders in the translated text
                element.textContent = translatedText
                    .replace("{name}", userName)
                    .replace("{age}", userAge)
                    .replace("{gender}", userGender);
            }
        }
    });
}

// Ensure translation occurs when page loads
document.addEventListener("DOMContentLoaded", applyTranslations);

// Handle language change event
document.addEventListener("DOMContentLoaded", function () {
    const languageSelect = document.getElementById("language");
    

    // Get stored language or default to English
    let selectedLanguage = localStorage.getItem("chatbotLanguage") || "english";
    languageSelect.value = selectedLanguage;

    // Change language on selection
    languageSelect.addEventListener("change", function () {
        let newLang = this.value;
        localStorage.setItem("chatbotLanguage", newLang);
        applyTranslations();
    });
});

